    private void btnIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIngresarActionPerformed
        // TODO add your handling code here:

        String campos = verificaCampos();
        //empleado: objeto clase empleado
        if (campos.equals("")) {
            switch (op) {
                case "Administrador" -> {
                    //empleado: objeto clase administrador
                    empleado = new Administrador();
                    
                    empleado.setNombreUsuario("admin");
                    empleado.setPassword("admin");
                    
                    listaEmpleados.agregarEmpleado(empleado);
                    
                    boolean login = listaEmpleados.login(txtUsuario.getText(), txtPassword.getText());

                    //instanciamos el frame que queremos que muestre
                    Jfrm_MenuAdministrador menuAdmin = new Jfrm_MenuAdministrador();
                    verificacionLogin(login, menuAdmin, this);// metodo que verifica el login y trae al frm
                    break;
                }
                case "Vendedor" -> {
                    //empleado: objeto clase vendedor
                    empleado = new Vendedor();
                    empleado.setNombreUsuario("vendedor");
                    empleado.setPassword("vendedor");
                    listaEmpleados.agregarEmpleado(empleado);
                    
                    boolean login = listaEmpleados.login(txtUsuario.getText(), txtPassword.getText());

                    //instanciamos el frame que queremos que muestree
                    Jfrm_MenuVendedor menuVendedor = new Jfrm_MenuVendedor();

                    verificacionLogin(login, menuVendedor, this);// metofo que verifica el login y trae el frm
                    break;
                }
                default -> {
                    JOptionPane.showMessageDialog(null, "Hubo un error. intentelo nuevamente");
                    break;
                }
            }
        } else JOptionPane.showMessageDialog(null, "Hubo un incoveniente con el campo: "+campos);

    }//GEN-LAST:event_btnIngresarActionPerformed

