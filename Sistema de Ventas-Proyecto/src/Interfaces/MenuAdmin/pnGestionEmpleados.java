    @Override
    public void placeHolder() {
        TextPrompt nombre = new TextPrompt("Nombre", txtNombreEmpleado);
        TextPrompt Apellido = new TextPrompt("Apellido", txtApellidosEmpleado);
        TextPrompt dni = new TextPrompt("DNI", txtDniEmpleado);
        TextPrompt telefono = new TextPrompt("Telefono Personal", txtTelefonoEmpleado);
        TextPrompt usuario = new TextPrompt("Usuario", txtUserEmpleado);
        TextPrompt contraseña = new TextPrompt("Contraseña", txtPassEmpleado);
    }

    @Override
    //agregamos un Empleado
    public void agregar() {
        String nombre = empleado.getNombre();
        String apellido = empleado.getApellido();
        String dni = empleado.getDni();
        String telefono = empleado.getTelefono();
        String nombreUsuario = empleado.getNombreUsuario();
        String password = empleado.getPassword();
        String tipo = empleado.getTipoEmpleado();
        //agregamos a la tabla
        mTablaEmpleados.addRow(new Object[]{
            mTablaEmpleados.getRowCount() + 1, nombre, apellido, dni, telefono, nombreUsuario, password, tipo
        });
    }

    private String verificaCampos() {
        if (txtNombreEmpleado.getText().equals("")) {
            return "Nombres";
        }
        if (txtApellidosEmpleado.getText().equals("")) {
            return "Apellidos";
        }
        if (txtDniEmpleado.getText().equals("")) {
            return "DNI";
        }
        if (txtTelefonoEmpleado.getText().equals("")) {
            return "Telefono";
        }
        if (txtUserEmpleado.getText().equals("")) {
            return "Usuario";
        }
        if (txtPassEmpleado.getText().equals("")) {
            return "Contraseña";
        }
        if (jboxTipoEmpleado.getSelectedIndex() == 0) {
            return "Tipo Empleado";
        }
        return "";
    }

    public void mostrarTiempo() {
        labelFecha.setText(tiempo.fechaCompleta);
        labelHora.setText(tiempo.horaCompleta);
    }

    void limpiaCampos() {
        txtApellidosEmpleado.setText("");
        txtDniEmpleado.setText("");
        txtNombreEmpleado.setText("");
        txtPassEmpleado.setText("");
        txtTelefonoEmpleado.setText("");
        txtUserEmpleado.setText("");
    }

