    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        // TODO add your handling code here:
        String tipoEmpleado = jboxTipoEmpleado.getSelectedItem().toString();

        if (!verificaCampos().equals("")) {
            JOptionPane.showMessageDialog(null, "Verifique los Dastos en el campo: " + verificaCampos());
        } else {
            switch (tipoEmpleado) {
                case "Administrador" -> {
                    empleado = new Administrador();
                    empleado.setNombre(txtNombreEmpleado.getText());
                    empleado.setApellido(txtApellidosEmpleado.getText());
                    empleado.setDni(txtDniEmpleado.getText());
                    empleado.setTelefono(txtTelefonoEmpleado.getText());
                    empleado.setNombreUsuario(txtUserEmpleado.getText());
                    empleado.setPassword(txtPassEmpleado.getText());
                    empleado.setTipoEmpleado(jboxTipoEmpleado.getSelectedItem().toString());

                    listaEmpleado.agregarEmpleado(empleado);
                    agregar();
                    limpiaCampos();
                    break;
                }
                case "Vendedor" -> {
                    empleado = new Vendedor();

                    empleado.setNombre(txtNombreEmpleado.getText());
                    empleado.setApellido(txtApellidosEmpleado.getText());
                    empleado.setDni(txtDniEmpleado.getText());
                    empleado.setTelefono(txtTelefonoEmpleado.getText());
                    empleado.setNombreUsuario(txtUserEmpleado.getText());
                    empleado.setPassword(txtPassEmpleado.getText());
                    empleado.setTipoEmpleado(jboxTipoEmpleado.getSelectedItem().toString());
                    
                    listaEmpleado.agregarEmpleado(empleado);
                    agregar();
                    limpiaCampos();
                    break;
                }
                default -> {
                    JOptionPane.showMessageDialog(null, "Hubo un error");
                    break;
                }
            }

        }

    }//GEN-LAST:event_btnRegistrarActionPerformed

