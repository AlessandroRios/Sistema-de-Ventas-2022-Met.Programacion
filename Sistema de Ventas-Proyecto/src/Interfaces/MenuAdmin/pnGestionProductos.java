    @Override
    //agregar producto
    public void agregar() {
        String nombre = producto.getNombre();
        String id = producto.getIdProducto();
        double precioUni = producto.getPrecioUnitario();
        int cantidad = producto.getCantidad();
        String marca = producto.getMarca();
        String categoria = producto.getCategoria();

        mTablaProducto.addRow(new Object[]{
            mTablaProducto.getRowCount() + 1, nombre, precioUni, categoria, marca, cantidad, id
        });
    }

    void limpiarCampos() {
        txtCantidad.setText("");
        txtIdProducto.setText("");
        txtMarca.setText("");
        txtNombreProducto.setText("");
        txtPrecioUni.setText("");
        jboxCategoriaProducto.setSelectedIndex(0);
    }

    private String verificaCampos() {
        if (txtCantidad.getText().equals("")) {
            return "Cantidad";
        }
        if (txtIdProducto.getText().equals("")) {
            return "ID producto";
        }
        if (txtMarca.getText().equals("")) {
            return "Marca";
        }
        if (txtNombreProducto.getText().equals("")) {
            return "Nombre Producto";
        }
        if (txtPrecioUni.getText().equals("")) {
            return "Precio Unitario";
        }
        if (jboxCategoriaProducto.getSelectedIndex() == 0) {
            return "Categoria del Producto";
        }
        return "";
    }

    public void mostrarTiempo() {
        labelFecha.setText(tiempo.fechaCompleta);
        labelHora.setText(tiempo.horaCompleta);
    }

