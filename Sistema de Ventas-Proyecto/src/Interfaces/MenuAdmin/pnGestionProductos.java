    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        // TODO add your handling code here:

        if (verificaCampos().equals("")) {
            producto.setNombre(txtNombreProducto.getText());
            producto.setIdProducto(txtIdProducto.getText());
            producto.setCantidad(Integer.parseInt(txtCantidad.getText()));
            producto.setPrecioUnitario(Double.parseDouble(txtPrecioUni.getText()));
            producto.setMarca(txtMarca.getText());
            producto.setCategoria(jboxCategoriaProducto.getSelectedItem().toString());
            //agregamos el producto
            
            agregar();//metodo interface paneles, para este caso es agregar productos.
            listaProducto.agregarProducto(producto);
            limpiarCampos();

        } else {
            JOptionPane.showMessageDialog(null, "Verifique los datos en: " + verificaCampos());
        }
    }//GEN-LAST:event_btnRegistrarActionPerformed

